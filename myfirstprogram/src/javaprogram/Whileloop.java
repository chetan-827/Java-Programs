package javaprogram;

public class Whileloop {
	public static void main(String[]args) {
		int i=5;
		while(i<=4)
		{
			System.out.println("hello"+i);
			int j=1;
			while(j<=3)
			{
				System.out.println("my name is chetan");
				j++;
			}
			i++;
		}
		System.out.println("bye"+i);
	}

}
